    #[test]
    fn model_env_forwarding_wrappers_delegate_to_shared_helpers() {
        let keys = [
            "MODEL_PROVIDER",
            "MODEL_GITHUB_TOKEN",
            "MODEL_SHELL_ALLOWLIST",
            "MODEL_AWS_EC2_METADATA_TOKEN",
            "MODEL_PROVIDER_BASE_URL",
        ];
        for key in keys {
            assert_eq!(
                is_forwardable_model_env_key(key),
                crate::host::sensitive_aliases::is_forwardable_model_env_key(key),
                "forwardable model key wrapper drift: {key}"
            );
        }

        let values = ["gpt-4.1", "openai", "", "bad value", "line\nfeed"];
        for value in values {
            assert_eq!(
                is_forwardable_model_env_value(value),
                crate::host::sensitive_aliases::is_forwardable_model_env_value(value),
                "forwardable model value wrapper drift: {value:?}"
            );
        }
    }

