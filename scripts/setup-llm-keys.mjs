#!/usr/bin/env node
/**
 * setup-llm-keys — configure API keys for pi-agent / tractor LLM providers
 *
 * Stores keys in .refarm/.env (gitignored).
 * Run any time to check status or update keys.
 *
 *   node scripts/setup-llm-keys.mjs          # interactive setup
 *   node scripts/setup-llm-keys.mjs --status # show current config, no changes
 */

import { existsSync, readFileSync, writeFileSync, mkdirSync, statSync } from 'node:fs';
import { createInterface } from 'node:readline';
import { spawnSync } from 'node:child_process';
import { join } from 'node:path';

const ROOT = new URL('..', import.meta.url).pathname.replace(/\/$/, '');
const ENV_FILE = join(ROOT, '.refarm', '.env');
const CONFIG_FILE = join(ROOT, '.refarm', 'config.json');

const c = {
  reset: '\x1b[0m', bold: '\x1b[1m', dim: '\x1b[2m',
  green: '\x1b[32m', red: '\x1b[31m', yellow: '\x1b[33m',
  cyan: '\x1b[36m', blue: '\x1b[34m', magenta: '\x1b[35m',
};

const STATUS_ONLY = process.argv.includes('--status');

// ── helpers ──────────────────────────────────────────────────────────────────

function readEnvFile() {
  if (!existsSync(ENV_FILE)) return {};
  const lines = readFileSync(ENV_FILE, 'utf8').split('\n');
  const vars = {};
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq < 0) continue;
    vars[trimmed.slice(0, eq)] = trimmed.slice(eq + 1);
  }
  return vars;
}

function writeEnvFile(vars) {
  mkdirSync(join(ROOT, '.refarm'), { recursive: true });
  const now = new Date().toISOString();
  const lines = [
    `# Refarm LLM provider keys — DO NOT COMMIT`,
    `# Generated by scripts/setup-llm-keys.mjs on ${now}`,
    `# Rotate keys at: https://console.anthropic.com/settings/keys (Anthropic)`,
    `#                 https://platform.openai.com/api-keys (OpenAI)`,
    ``,
  ];
  for (const [k, v] of Object.entries(vars)) {
    lines.push(`${k}=${v}`);
  }
  writeFileSync(ENV_FILE, lines.join('\n') + '\n');
}

function maskedKey(key) {
  if (!key || key.length < 12) return '(empty)';
  return key.slice(0, 10) + '...' + key.slice(-4);
}

function envFileMtime() {
  try { return statSync(ENV_FILE).mtime; } catch { return null; }
}

async function prompt(rl, question) {
  return new Promise(resolve => rl.question(question, resolve));
}

async function validateAnthropicKey(key) {
  if (!key || !key.startsWith('sk-ant-')) return { ok: false, reason: 'invalid prefix (expected sk-ant-)' };
  const r = spawnSync('curl', [
    '-sf', '--max-time', '5',
    '-H', `x-api-key: ${key}`,
    '-H', 'anthropic-version: 2023-06-01',
    '-H', 'content-type: application/json',
    '-d', JSON.stringify({ model: 'claude-haiku-4-5-20251001', max_tokens: 1, messages: [{ role: 'user', content: 'hi' }] }),
    'https://api.anthropic.com/v1/messages',
  ], { encoding: 'utf8' });

  if (r.status !== 0) return { ok: false, reason: 'network error or curl unavailable' };
  try {
    const body = JSON.parse(r.stdout);
    if (body.error?.type === 'authentication_error') return { ok: false, reason: 'key rejected by Anthropic API' };
    if (body.id || body.content) return { ok: true };
    return { ok: false, reason: body.error?.message ?? 'unexpected response' };
  } catch {
    return { ok: false, reason: 'could not parse API response' };
  }
}

async function validateOpenAIKey(key) {
  if (!key || !key.startsWith('sk-')) return { ok: false, reason: 'invalid prefix (expected sk-)' };
  const r = spawnSync('curl', [
    '-sf', '--max-time', '5',
    '-H', `Authorization: Bearer ${key}`,
    'https://api.openai.com/v1/models',
  ], { encoding: 'utf8' });

  if (r.status !== 0) return { ok: false, reason: 'network error or curl unavailable' };
  try {
    const body = JSON.parse(r.stdout);
    if (body.error?.code === 'invalid_api_key') return { ok: false, reason: 'key rejected by OpenAI API' };
    if (Array.isArray(body.data)) return { ok: true };
    return { ok: false, reason: body.error?.message ?? 'unexpected response' };
  } catch {
    return { ok: false, reason: 'could not parse API response' };
  }
}

// ── display ───────────────────────────────────────────────────────────────────

function printHeader() {
  console.log(`\n${c.bold}${c.cyan}🔑 Refarm LLM Key Setup${c.reset}`);
  console.log(`${c.dim}Keys are stored in ${ENV_FILE.replace(ROOT, '.')} (gitignored)${c.reset}\n`);
}

function printProvider(name, key, valid, reason) {
  const icon = !key ? '○' : valid ? '✓' : '✗';
  const col = !key ? c.dim : valid ? c.green : c.red;
  const status = !key ? 'not configured' : valid ? 'valid' : `invalid — ${reason}`;
  console.log(`  ${col}${icon}${c.reset} ${c.bold}${name}${c.reset}  ${c.dim}${maskedKey(key)}${c.reset}  ${col}${status}${c.reset}`);
}

function printLinks() {
  console.log(`\n${c.dim}Generate keys at:${c.reset}`);
  console.log(`  ${c.blue}Anthropic${c.reset}  https://console.anthropic.com/settings/keys`);
  console.log(`  ${c.blue}OpenAI${c.reset}     https://platform.openai.com/api-keys`);
}

function openBrowser(url) {
  const opener = process.platform === 'darwin' ? 'open'
    : process.platform === 'win32' ? 'start'
    : 'xdg-open';
  spawnSync(opener, [url], { stdio: 'ignore' });
}

function printUsage(vars) {
  const provider = vars.LLM_PROVIDER ?? 'ollama';
  console.log(`\n${c.dim}To start pi-agent with these keys:${c.reset}`);
  console.log(`  ${c.cyan}source .refarm/.env${c.reset}`);
  console.log(`  ${c.cyan}export LLM_PROVIDER=${provider}${c.reset}`);
  console.log(`  ${c.cyan}packages/tractor/target/release/tractor \\${c.reset}`);
  console.log(`  ${c.cyan}  --plugin packages/pi-agent/target/wasm32-wasip1/release/pi_agent.wasm${c.reset}`);
  console.log(`\n${c.dim}Or use the wrapper:${c.reset}  ${c.cyan}./scripts/tractor-start.sh${c.reset}\n`);
}

function printKeyAge() {
  const mtime = envFileMtime();
  if (!mtime) return;
  const days = Math.floor((Date.now() - mtime.getTime()) / (1000 * 60 * 60 * 24));
  const col = days > 180 ? c.yellow : c.dim;
  console.log(`  ${col}Keys last updated ${days} day(s) ago (${mtime.toISOString().slice(0, 10)})${c.reset}`);
  if (days > 180) {
    console.log(`  ${c.yellow}⚠  Consider rotating keys for security hygiene (> 6 months old)${c.reset}`);
  }
}

// ── main ──────────────────────────────────────────────────────────────────────

async function main() {
  printHeader();

  const existing = readEnvFile();
  const anthropicKey = existing.ANTHROPIC_API_KEY ?? process.env.ANTHROPIC_API_KEY ?? '';
  const openaiKey = existing.OPENAI_API_KEY ?? process.env.OPENAI_API_KEY ?? '';

  // Validate existing keys
  process.stdout.write(`${c.dim}Checking configured keys...${c.reset}\n`);
  const [aResult, oResult] = await Promise.all([
    anthropicKey ? validateAnthropicKey(anthropicKey) : Promise.resolve({ ok: false, reason: 'not configured' }),
    openaiKey ? validateOpenAIKey(openaiKey) : Promise.resolve({ ok: false, reason: 'not configured' }),
  ]);

  printProvider('Anthropic', anthropicKey, aResult.ok, aResult.reason);
  printProvider('OpenAI   ', openaiKey, oResult.ok, oResult.reason);
  printKeyAge();
  printLinks();

  if (STATUS_ONLY) {
    if (!aResult.ok && !oResult.ok) {
      console.log(`\n${c.yellow}Run without --status to configure keys.${c.reset}\n`);
    }
    return;
  }

  // Interactive setup
  const needsSetup = !aResult.ok || !oResult.ok;
  if (!needsSetup) {
    console.log(`\n${c.green}All configured providers are valid. Nothing to do.${c.reset}`);
    printUsage(existing);
    return;
  }

  console.log(`\n${c.bold}Configure keys${c.reset} (press Enter to skip a provider)\n`);

  const rl = createInterface({ input: process.stdin, output: process.stdout });
  const updated = { ...existing };

  if (!aResult.ok) {
    const anthropicUrl = 'https://console.anthropic.com/settings/keys';
    console.log(`  ${c.dim}Opening browser → ${anthropicUrl}${c.reset}`);
    openBrowser(anthropicUrl);
    const input = (await prompt(rl, `  Paste your Anthropic API key (sk-ant-...): `)).trim();
    if (input) {
      process.stdout.write(`  ${c.dim}Validating...${c.reset} `);
      const r = await validateAnthropicKey(input);
      if (r.ok) {
        console.log(`${c.green}✓ valid${c.reset}`);
        updated.ANTHROPIC_API_KEY = input;
      } else {
        console.log(`${c.red}✗ ${r.reason}${c.reset}`);
        console.log(`  ${c.dim}Key NOT saved. Re-run to try again.${c.reset}`);
      }
    }
  }

  if (!oResult.ok) {
    const openaiUrl = 'https://platform.openai.com/api-keys';
    console.log(`  ${c.dim}Opening browser → ${openaiUrl}${c.reset}`);
    openBrowser(openaiUrl);
    const input = (await prompt(rl, `  Paste your OpenAI API key (sk-...): `)).trim();
    if (input) {
      process.stdout.write(`  ${c.dim}Validating...${c.reset} `);
      const r = await validateOpenAIKey(input);
      if (r.ok) {
        console.log(`${c.green}✓ valid${c.reset}`);
        updated.OPENAI_API_KEY = input;
      } else {
        console.log(`${c.red}✗ ${r.reason}${c.reset}`);
        console.log(`  ${c.dim}Key NOT saved. Re-run to try again.${c.reset}`);
      }
    }
  }

  rl.close();

  // Persist if anything changed
  const changed = JSON.stringify(updated) !== JSON.stringify(existing);
  if (changed) {
    writeEnvFile(updated);
    console.log(`\n${c.green}✓ Saved to ${ENV_FILE.replace(ROOT + '/', '')}${c.reset}`);
  } else {
    console.log(`\n${c.dim}No changes made.${c.reset}`);
  }

  printUsage(updated);
}

main().catch(e => { console.error(e); process.exit(1); });
